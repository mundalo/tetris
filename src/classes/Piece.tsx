export interface Piece {
    type: number;
    tetrimino: number[][][];
}

export const generatePiece = (): Piece => {
    const type = Math.floor(Math.random() * 7) + 1;
    const tetrimino = getTetriminos(type);

    return { type, tetrimino };
};

export const getPieceColor = (piece_nbr: number): string => {
    switch (piece_nbr) {
        case 0:
            return "#dfdfdf";
        case 1:
            return "#ca0000";
        case 2:
            return "#c79800";
        case 3:
            return "#00ac3f";
        case 4:
            return "#0058ca";
        case 5:
            return "#9300e9";
        case 6:
            return "#ca00af";
        case 7:
            return "#87da00";
        default: {
            throw new Error(`Unhandled piece case: ${piece_nbr}`);
        }
    }
};

