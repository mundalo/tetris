export var getClassName = function (piece_nbr) {
    switch (piece_nbr) {
        case 1:
            return 'piece-1';
        case 2:
            return 'piece-2';
        case 3:
            return 'piece-3';
        case 4:
            return 'piece-4';
        case 5:
            return 'piece-5';
        case 6:
            return 'piece-6';
        case 7:
            return 'piece-7';
        case 'ghost':
            return 'piece-preview';
        default: {
            var exhaustiveCheck = piece_nbr;
            throw new Error("Unhandled piece case: ".concat(exhaustiveCheck));
        }
    }
};
